#include "libft.h"

char	*ft_strndup(const char *s1, size_t n)
{
	char				*dest;
	unsigned long		i;

	i = 0;
	if (!(dest = (char*)malloc(n * sizeof(char) + 1)))
		return (NULL);
	while (s1[i] != '\0' && i < n)
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
